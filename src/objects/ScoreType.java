package objects;

public class ScoreType {
	public Integer ScoreTypeId;
	public String Name; 
	public Float Rate; 
	
	public void setScoreTypeId (Integer ScoreTypeId) {
		this.ScoreTypeId = ScoreTypeId;
	}
	
	public void setName (String Name) {
		this.Name = Name;
	}
	
	public void setRate (Float Rate) {
		this.Rate = Rate;
	}
}
